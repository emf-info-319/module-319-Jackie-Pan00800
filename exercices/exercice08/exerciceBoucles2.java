package exercice08;

public class exerciceBoucles2 {
    public static void main(String[] args) {
        int nbr=1;
        boolean uneCondition=true;
        while (uneCondition=true){
            System.out.println(nbr++);
            if (nbr>5){
                uneCondition=false;
                System.out.println(uneCondition);
                if (uneCondition=false);{
                    break;
                }
            }
        }
    }

    
}
