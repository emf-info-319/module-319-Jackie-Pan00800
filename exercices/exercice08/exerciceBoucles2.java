public class exerciceBoucles2 {
    public static void main(String[] args) {
        int nbr=0;
        boolean uneCondition=true;
        while (uneCondition=true){
            System.out.println(nbr+=1);
            if (nbr==5){
                uneCondition=false;
                if (uneCondition=false);{
                    break;
                }
            }
        }
    }

    
}
